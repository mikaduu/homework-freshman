void convert(int n)
{
	int yushu;
	if (n < 0 && n != -2147483648)//防止-2147483648连续输出两个负号
	{

		cout << "- ";
		n = -n;//防止负号反复输出
	}

	if (n < 10 && n != -2147483648)
	{
		cout << char('0' + n) << " ";
		return;
	}
	convert(n / 10);
	yushu = n % 10;
	if (yushu < 0)
	{
		yushu = -yushu;
	}
	cout << char('0' + yushu) << " ";
	return;
}
